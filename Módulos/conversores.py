def celsius_para_fahrenheit(grau):
    return ((grau*1.8)+32)

def fahrenheit_para_celsius(grau):
    return ((grau-32)/1.8)

def metro_para_pes(metro):
    return(metro*3.281)

def pes_para_metro(pes):
    return(pes/3.281)

def quilometro_para_milhas(km):
    return(km/1.609)

def milhas_para_quilometro(milha):
    return (milha*1.609)

def dia_para_horas(dia):
    return (dia * 24)
