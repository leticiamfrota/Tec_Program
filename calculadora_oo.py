class Calculadora:
    def soma(self, operando_a, operando_b):
        return (operando_a + operando_b)
    def subtracao(self, operando_a, operando_b):
        return (operando_a - operando_b)
    def multiplicacao(self, operando_a, operando_b):
        return (operando_a * operando_b)
    def divisao(self, operando_a, operando_b):
        return (operando_a / operando_b)
